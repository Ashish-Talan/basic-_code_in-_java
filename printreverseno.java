import java.util.*;
public class printreverseno {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.print("enter last no : ");
        int n=sc.nextInt();
        int i=1;
        do {
            System.out.print(i+" ");
            i++;

        }while(i<=n);
    }
    
}
